public class LoopWithCounter {
    public static void main(String[] args) {
        int number = 1;
        while (number < 6) {
            System.out.println("값은" + " " + number + "입니다.");
            number = number + 1;
        }
        System.out.println("종료되었습니다.");
        // return 0 ;

    }

}